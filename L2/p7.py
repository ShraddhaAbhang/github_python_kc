# wapp to demo the diff operators in python

a, b = 5, 8

print(a)
print(b)

print(a + b)        # + ==> addition
print(a - b)        # - ==> substraction
print(b - a)        # - ==> substraction
print(a * b)        # * ==> multiplication
print(b * a)        # * ==> multiplication
print(b / a)        # / ==> division
print(b // a)       # // ==> floor division, only gives integer
print(a / b)        # / ==> division
print(a // b)       # // ==> floor division, only gives integer
print(b % a)        # % ==> module / reminder
print(a % b)        # % ==> module / reminder

